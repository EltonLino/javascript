// Estrutura de repetição com teste lógico no início - while
var c = 1;
while (c <= 5) {
    console.log(`Passo ${c}`);
    c++;
}

console.log('------------------');
// Estrutura de repetição com teste lógico no final - do...while
var c = 1;
do {
    console.log(`Passo ${c}`);
    c++;
} while (c <= 5);

console.log('------------------');
// Estrutura de repetição com variável de controle - for
for (var c = 1; c <= 5; c++) {
    console.log(`Passo ${c}`);
}